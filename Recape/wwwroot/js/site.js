import Home from "./home.js";

$(() => {
    $('a[href^="#"]').click(function (event) {
        event.preventDefault();

        $('html, body').animate({
            scrollTop: $($.attr(this, 'href')).offset().top
        }, 1000);
    });

    Home();
});